package screens.recursos;

import java.awt.Color;

public class Cores {
    private Color azulEscuro;
    private Color azulMedio;
    private Color azulVerde;
    private Color azulClaro;
    private Color branco;

    public Cores() {
        this.azulEscuro = new Color(19,41,61);
        this.azulMedio = new Color(0,100,148);
        this.azulVerde = new Color(36,123,160);
        this.azulClaro = new Color(27, 152, 224);
        this.branco = new Color(232, 241, 242);
    }

    public Color getAzulEscuro() {
        return azulEscuro;
    }

    public Color getAzulMedio() {
        return azulMedio;
    }

    public Color getAzulVerde() {
        return azulVerde;
    }

    public Color getAzulClaro() {
        return azulClaro;
    }

    public Color getBranco() {
        return branco;
    }

    
    

}
